package week1.day2;

public class Factorial {

	public static void main(String[] args) {
	       int n=5;
	       int fact=1;//1  2  6
	       //loop
	       for (int i = 1; i <=n ; i++) {//1 2 3 4 5
			fact=fact*i;//1=1*1   2=2*1 6=3*2
	    	   
		}
	       System.out.println(fact);
		
	}

}
